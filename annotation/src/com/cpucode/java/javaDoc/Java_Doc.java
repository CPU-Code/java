public class Java_Doc {

    /**
     * 计算两数的和
     * @param a 整数
     * @param b 整数
     * @return 两数的和
     */

    /****
     * @Description: add
     *   * @param a
     * @param b
     * @return: int
     * @Author: cpucode
     * @Date: 2020/11/9
     */
    public int add(int a, int b ){
        return a + b;
    }


    /**
     * @Description: summ
     * @Param: [a, b]
     * @return: int
     * @Author: cpucode
     * @Date: 2020/11/9    tab+a+/
     */


    public  int summ(int a,int b){
        return a+b;
    }
}

